package edu.nju;

public class User {
	private int tweetnum;
	private String name;
	
	public User(){
		
	}
	
	public int getTweetnum() {
		return tweetnum;
	}

	public String getName() {
		return name;
	}

	public void setTweetnum(int tweetnum) {
		this.tweetnum = tweetnum;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	
}
